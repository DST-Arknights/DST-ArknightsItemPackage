local SafeCallArkExtendUI = GenSafeCall(function (inst)
  return inst and inst.HUD and inst.HUD.controls and inst.HUD.controls.arkExtendUi or nil
end)
local SafeCallArkEliteUI = GenSafeCall(function (inst)
  return SafeCallArkExtendUI(inst).elite
end)
local SafeCallArkExpBarUI = GenSafeCall(function (inst)
  return SafeCallArkExtendUI(inst).expBar
end)
local ArkElite = Class(function(self, inst)
  self.inst = inst

  -- NetState 定义：组件只写入这些字段，UI 只从这里读取
  -- 注意：levelUpExp 不再通过网络同步，改为本地计算
  local stateDef = {
    rarity = "int:classified",
    potential = "int:classified",
    elite = "int:classified",
    level = "int:classified",
    currentExp = "int:classified",
    totalExp = "int:classified",
    overflowExp = "int:classified",
  }

  self.state = NetState(self.inst, stateDef)
  self.state:Attach(self.inst)

  if not TheNet:IsDedicated() then
    SafeCallArkExtendUI(self.inst):SetupElite()
    -- 监听核心字段变化，驱动 ArkEliteUI（包括多级连升与减速逻辑）
    local watched = {"rarity", "potential", "elite", "level", "currentExp", "overflowExp"}
    self.state:Watch(watched, function()
      ArkLogger:Debug('ark_elite_replica Watch OnDirty', self.state.rarity, self.state.potential, self.state.elite, self.state.level, self.state.currentExp, self.state.overflowExp)
      SafeCallArkExtendUI(self.inst):OnEliteDirty(self.state)
    end)
  end
end)

-- 计算下一级所需经验：不走网络，直接根据当前（或指定）等级计算
function ArkElite:GetLevelUpExp(level)
  local elite = self.state.elite or 1
  local lvl = level or self.state.level or 1
  local need = EXP_CONFIG.exp[elite] and EXP_CONFIG.exp[elite][lvl] and EXP_CONFIG.exp[elite][lvl][1] or 1
  return need
end

-- 是否已达当前精英化阶段的封顶等级
function ArkElite:IsAtLevelCap()
  return self.state.level >= self:GetLevelCap()
end

-- 当前精英化阶段的封顶等级
function ArkElite:GetLevelCap()
  local elite = self.state.elite or 1
  local rarity = self.state.rarity
  -- rarity 可能是 0 或 nil，都视为无效，默认 1
  if not rarity or rarity < 1 then
    rarity = 1
  end
  return EXP_CONFIG.maxLevel[rarity] and EXP_CONFIG.maxLevel[rarity][elite] or 1
end
return ArkElite

